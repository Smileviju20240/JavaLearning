package collectionsConcept;

public class Employee {
	
	int emp_Id;
	String emp_Name;
	int dept_Id;
	boolean e_Active;
	
	public Employee(int emp_Id, String emp_Name, int dept_Id, boolean e_Active) {
		this.emp_Id = emp_Id;
		this.emp_Name = emp_Name;
		this.dept_Id = dept_Id;
		this.e_Active = e_Active;
	}
	
}
